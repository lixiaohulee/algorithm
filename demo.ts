//实现x的n次方，n为整 -2 2

const func = (x: number, n: number): number => {
  let res: number = 1;

  for (let i = 0; i < Math.abs(n); i++) { //On
    res = res * x;
  }

  return n < 0 ? 1 / res : res;
}

